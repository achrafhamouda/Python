import math

# Utilisation de math
racine_carree = math.sqrt(196)
valeur_pi = round(math.pi, 4)
sinus_90 = math.sin(math.radians(90))

print("Racine carrée de 196 :", racine_carree)
print("Valeur de π arrondie à 4 chiffres :", valeur_pi)
print("Sinus de 90° :", sinus_90)

import datetime
# Utilisation de datetime
date_actuelle = datetime.date.today()
heure_actuelle = datetime.datetime.now().strftime("%H:%M:%S")
print("Date actuelle :", date_actuelle)
print("Heure actuelle :", heure_actuelle)