# Liste donnée
nums = [10, 20, 30, 40, 50, 60, 70, 80, 90]

# Afficher les éléments du 80 jusqu’au 10 en sens inverse
print(nums[7::-1])

# Afficher les éléments de 50 à 80
print(nums[4:8])

# Afficher un élément sur deux en partant de 90 vers la gauche
print(nums[::-2])

# Afficher les éléments à partir de 30, un sur deux
print(nums[2::2])

# Afficher la liste inversée complètement
print(nums[::-1])

# Afficher les trois derniers éléments 70, 80, 90
print(nums[6:9])

# En partant de 90, afficher un élément sur trois vers la gauche
print(nums[8::-3])